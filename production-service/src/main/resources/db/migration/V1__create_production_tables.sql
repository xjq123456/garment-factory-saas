-- 3. 工序步骤表
CREATE TABLE IF NOT EXISTS prod_process_step (
    id              BIGINT          NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    tenant_id       BIGINT          NOT NULL DEFAULT 0 COMMENT '租户ID',
    route_id        BIGINT          NOT NULL COMMENT '工艺路线ID',
    step_no         INT             NOT NULL COMMENT '工序序号',
    step_name       VARCHAR(128)    NOT NULL COMMENT '工序名称',
    step_type       VARCHAR(32)     NOT NULL DEFAULT 'SEWING' COMMENT '工序类型: CUTTING-裁剪, IRONING-熨烫, SEWING-缝制, PACKING-包装, QC-质检, OTHER-其他',
    standard_time   DECIMAL(10,2)   DEFAULT NULL COMMENT '标准工时(分钟)',
    standard_output INT             DEFAULT NULL COMMENT '标准产量(件/小时)',
    description     VARCHAR(512)    DEFAULT NULL COMMENT '工序说明',
    create_by       VARCHAR(64)     DEFAULT NULL COMMENT '创建人',
    create_time     DATETIME        DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_by       VARCHAR(64)     DEFAULT NULL COMMENT '更新人',
    update_time     DATETIME        DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted         TINYINT         NOT NULL DEFAULT 0 COMMENT '逻辑删除 0-未删除 1-已删除',
    PRIMARY KEY (id),
    KEY idx_route_id (route_id),
    KEY idx_tenant (tenant_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='工序步骤表';

-- 4. 工位表
CREATE TABLE IF NOT EXISTS prod_workstation (
    id              BIGINT          NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    tenant_id       BIGINT          NOT NULL DEFAULT 0 COMMENT '租户ID',
    station_code    VARCHAR(64)     NOT NULL COMMENT '工位编号',
    station_name    VARCHAR(128)    NOT NULL COMMENT '工位名称',
    workshop        VARCHAR(64)     DEFAULT NULL COMMENT '车间',
    production_line VARCHAR(64)     DEFAULT NULL COMMENT '产线',
    station_type    VARCHAR(32)     NOT NULL DEFAULT 'SEWING' COMMENT '工位类型: CUTTING-裁剪, IRONING-熨烫, SEWING-缝制, PACKING-包装, QC-质检, OTHER-其他',
    worker_id       BIGINT          DEFAULT NULL COMMENT '当前绑定工人ID',
    worker_name     VARCHAR(64)     DEFAULT NULL COMMENT '当前绑定工人姓名',
    equipment_code  VARCHAR(64)     DEFAULT NULL COMMENT '设备编号',
    status          VARCHAR(32)     NOT NULL DEFAULT 'IDLE' COMMENT '状态: IDLE-空闲, BUSY-忙碌, MAINTENANCE-维护中, DISABLED-停用',
    remark          VARCHAR(512)    DEFAULT NULL COMMENT '备注',
    create_by       VARCHAR(64)     DEFAULT NULL COMMENT '创建人',
    create_time     DATETIME        DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_by       VARCHAR(64)     DEFAULT NULL COMMENT '更新人',
    update_time     DATETIME        DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted         TINYINT         NOT NULL DEFAULT 0 COMMENT '逻辑删除 0-未删除 1-已删除',
    version         INT             NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
    PRIMARY KEY (id),
    UNIQUE KEY uk_station_code (station_code, tenant_id),
    KEY idx_tenant (tenant_id),
    KEY idx_workshop (workshop),
    KEY idx_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='工位表';

-- 5. 生产任务表
CREATE TABLE IF NOT EXISTS prod_production_task (
    id              BIGINT          NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    tenant_id       BIGINT          NOT NULL DEFAULT 0 COMMENT '租户ID',
    task_no         VARCHAR(64)     NOT NULL COMMENT '任务编号',
    order_id        BIGINT          NOT NULL COMMENT '生产工单ID',
    route_id        BIGINT          DEFAULT NULL COMMENT '工艺路线ID',
    step_id         BIGINT          NOT NULL COMMENT '工序步骤ID',
    step_name       VARCHAR(128)    NOT NULL COMMENT '工序名称',
    station_id      BIGINT          DEFAULT NULL COMMENT '分配工位ID',
    station_name    VARCHAR(128)    DEFAULT NULL COMMENT '工位名称',
    worker_id       BIGINT          DEFAULT NULL COMMENT '工人ID',
    worker_name     VARCHAR(64)     DEFAULT NULL COMMENT '工人姓名',
    plan_qty        INT             NOT NULL DEFAULT 0 COMMENT '计划数量',
    completed_qty   INT             NOT NULL DEFAULT 0 COMMENT '完成数量',
    defective_qty   INT             NOT NULL DEFAULT 0 COMMENT '不良品数量',
    unit            VARCHAR(16)     NOT NULL DEFAULT '件' COMMENT '单位',
