    actual_start_time DATETIME      DEFAULT NULL COMMENT '实际开始时间',
    actual_end_time DATETIME        DEFAULT NULL COMMENT '实际结束时间',
    status          VARCHAR(32)     NOT NULL DEFAULT 'PENDING' COMMENT '状态: PENDING-待分配, ASSIGNED-已分配, IN_PROGRESS-进行中, PAUSED-暂停, COMPLETED-已完成, CANCELLED-已取消',
    priority        TINYINT         NOT NULL DEFAULT 0 COMMENT '优先级 0-普通 1-加急 2-特急',
    remark          VARCHAR(512)    DEFAULT NULL COMMENT '备注',
    create_by       VARCHAR(64)     DEFAULT NULL COMMENT '创建人',
    create_time     DATETIME        DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_by       VARCHAR(64)     DEFAULT NULL COMMENT '更新人',
    update_time     DATETIME        DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted         TINYINT         NOT NULL DEFAULT 0 COMMENT '逻辑删除 0-未删除 1-已删除',
    version         INT             NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
    PRIMARY KEY (id),
    UNIQUE KEY uk_task_no (task_no, tenant_id),
    KEY idx_order_id (order_id),
    KEY idx_station_id (station_id),
    KEY idx_worker_id (worker_id),
    KEY idx_tenant_status (tenant_id, status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='生产任务表';

-- 6. 质检记录表
CREATE TABLE IF NOT EXISTS prod_quality_inspection (
    id              BIGINT          NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    tenant_id       BIGINT          NOT NULL DEFAULT 0 COMMENT '租户ID',
    inspection_no   VARCHAR(64)     NOT NULL COMMENT '质检编号',
    order_id        BIGINT          NOT NULL COMMENT '生产工单ID',
    task_id         BIGINT          DEFAULT NULL COMMENT '生产任务ID',
    inspection_type VARCHAR(32)     NOT NULL DEFAULT 'INLINE' COMMENT '质检类型: INLINE-巡检, FINAL-终检, INCOMING-来料检',
    inspect_qty     INT             NOT NULL DEFAULT 0 COMMENT '检验数量',
    pass_qty        INT             NOT NULL DEFAULT 0 COMMENT '合格数量',
    reject_qty      INT             NOT NULL DEFAULT 0 COMMENT '不合格数量',
    defect_types    VARCHAR(512)    DEFAULT NULL COMMENT '缺陷类型(JSON数组)',
    defect_desc     VARCHAR(1024)   DEFAULT NULL COMMENT '缺陷描述',
    result          VARCHAR(32)     NOT NULL DEFAULT 'PENDING' COMMENT '结果: PENDING-待检, PASS-合格, REJECT-不合格, CONDITIONAL-让步接收',
    inspector_id    BIGINT          DEFAULT NULL COMMENT '质检员ID',
    inspector_name  VARCHAR(64)     DEFAULT NULL COMMENT '质检员姓名',
    inspection_time DATETIME        DEFAULT NULL COMMENT '质检时间',
    remark          VARCHAR(512)    DEFAULT NULL COMMENT '备注',
    create_by       VARCHAR(64)     DEFAULT NULL COMMENT '创建人',
    create_time     DATETIME        DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_by       VARCHAR(64)     DEFAULT NULL COMMENT '更新人',
    update_time     DATETIME        DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted         TINYINT         NOT NULL DEFAULT 0 COMMENT '逻辑删除 0-未删除 1-已删除',
    PRIMARY KEY (id),
    UNIQUE KEY uk_inspection_no (inspection_no, tenant_id),
    KEY idx_order_id (order_id),
    KEY idx_task_id (task_id),
    KEY idx_tenant_result (tenant_id, result)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='质检记录表';

