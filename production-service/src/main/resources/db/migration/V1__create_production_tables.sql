-- 1. 生产工单表
CREATE TABLE IF NOT EXISTS prod_production_order (
    id              BIGINT          NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    tenant_id       BIGINT          NOT NULL DEFAULT 0 COMMENT '租户ID',
    order_no        VARCHAR(64)     NOT NULL COMMENT '工单编号',
    style_id        BIGINT          NOT NULL COMMENT '款式ID',
    style_code      VARCHAR(64)     NOT NULL COMMENT '款式编号',
    style_name      VARCHAR(128)    NOT NULL COMMENT '款式名称',
    sku_id          BIGINT          DEFAULT NULL COMMENT 'SKU ID',
    sku_code        VARCHAR(64)     DEFAULT NULL COMMENT 'SKU编号',
    customer_name   VARCHAR(128)    DEFAULT NULL COMMENT '客户名称',
    total_qty       INT             NOT NULL DEFAULT 0 COMMENT '计划生产总数',
    completed_qty   INT             NOT NULL DEFAULT 0 COMMENT '已完成数量',
    defective_qty   INT             NOT NULL DEFAULT 0 COMMENT '不良品数量',
    unit            VARCHAR(16)     NOT NULL DEFAULT '件' COMMENT '单位',
    delivery_date   DATE            DEFAULT NULL COMMENT '交货日期',
    start_date      DATE            DEFAULT NULL COMMENT '计划开始日期',
    end_date        DATE            DEFAULT NULL COMMENT '计划结束日期',
    priority        TINYINT         NOT NULL DEFAULT 0 COMMENT '优先级 0-普通 1-加急 2-特急',
    status          VARCHAR(32)     NOT NULL DEFAULT 'CREATED' COMMENT '状态: CREATED-已创建, APPROVED-已审批, IN_PROGRESS-生产中, SUSPENDED-暂停, COMPLETED-已完成, CLOSED-已关闭',
    remark          VARCHAR(512)    DEFAULT NULL COMMENT '备注',
    create_by       VARCHAR(64)     DEFAULT NULL COMMENT '创建人',
    create_time     DATETIME        DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_by       VARCHAR(64)     DEFAULT NULL COMMENT '更新人',
    update_time     DATETIME        DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted         TINYINT         NOT NULL DEFAULT 0 COMMENT '逻辑删除 0-未删除 1-已删除',
    version         INT             NOT NULL DEFAULT 0 COMMENT '乐观锁版本号',
    PRIMARY KEY (id),
    UNIQUE KEY uk_order_no (order_no, tenant_id),
    KEY idx_tenant_status (tenant_id, status),
    KEY idx_style_id (style_id),
    KEY idx_delivery_date (delivery_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='生产工单表';

-- 2. 工艺路线表
